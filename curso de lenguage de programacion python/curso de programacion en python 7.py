#operadores relacionales
#operadores aritmeticos se utilizan para la relacion entre 2 valores compara valores entre si y produce el resultado de certeza o falsedad(verdadero o falso)
#tienen el mismo nivel de prioridad en su evaluacion   
# se representan de la siguiente manera mayor que > < menor que >=mayor o igual que <= menor o igual que !=diferente  igual ==
resultado = 10 <  20 # te da valor booleano verdadero (true)menor que
print(resultado)
resultado = 10 > 20# te da valor booleano falso (false)mayor que
print(resultado)
resultado = 10 == 10#te da el valor booleano igual 
print(resultado)
resultado = 10 != 20#te da el valor booleano de diferencia
print(resultado)
resultado = 20 <= 15#te da el valor booleano menor o igual ya que si no se cumple el valor menor o igual es falso
print(resultado)
resultado = 10 >= 20#te da el valor booleano mayor o igual ya que si no se cumple el valor menor o igual es falso
print(resultado)

